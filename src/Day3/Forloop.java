package Day3;

public class Forloop {

	public static void main(String[] args) {
		
		//program to print first five natural number
	
		
		for (int i = 1; i<=5;i++){
			System.out.println(i);
		}
	
		// program to print sum of natural number from 1 to 10
		
		int sum = 0;
		
		for (int i = 1; i <=10000; i++) {
			sum = sum + i;
		}
		System.out.println(sum);
	
		
		
		
	}
}

