package Day2;

public class AssignmentOperators {

	public static void main(String[] args) {
		// case 1: +=
		
		int a = 10;
		a += 5; 	//a = a+5
		System.out.println("The value of a is "+a);	//15
		
		// Case 2: -=
		
		int b = 100;
		b -= 20;	//b = b - 1
		System.out.println("The value of b is "+ b);	//80
		
		//Case 3: *=
		
		int c = 50;
		c *=10;		//c = c * 10
		System.out.println("The value of c is "+ c);
		
		//case 4: /=
		
		int d = 500;
		d /=50;		//d = d / 50
		System.out.println("The value of d is "+ d);
		
		//case 5: %=
		
		int e  = 100;
		e %= 11;		//e = e % 11
		System.out.println("The value of e is "+e);
		
		
	}

}
