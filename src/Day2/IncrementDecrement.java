package Day2;

public class IncrementDecrement {

	public static void main(String[] args) {
		int a = 5, b = 100, c = 95, d = 200;
		a++; 	//a = a + 1
		b++;	//b + b + 1
		System.out.println(a);
		System.out.println(b);
		
		c--; 	//c = c - 1
		d--;	//d = d - 1
		
		System.out.println(c);
		System.out.println(d);
		

	}

}
